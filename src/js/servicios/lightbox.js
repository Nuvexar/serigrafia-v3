let filteredImages = [...galleryImages]; // por defecto todas

function openLightbox(index) {
  currentImageIndex = index;
  lightboxImage.src = filteredImages[index];
  lightbox.classList.add("active");
  document.body.style.overflow = "hidden";
}
function closeLightbox() {
  lightbox.classList.remove("active");
  document.body.style.overflow = ""; // Restaurar scroll
}

function changeImage(direction) {
    currentImageIndex = (currentImageIndex + direction + filteredImages.length) % filteredImages.length;
    lightboxImage.src = filteredImages[currentImageIndex];
}

// Cerrar lightbox al hacer clic fuera de la imagen
lightbox.addEventListener("click", function (e) {
  if (e.target === lightbox) {
    closeLightbox();
  }
});

// Teclas de navegación para el lightbox
document.addEventListener("keydown", function (e) {
  if (!lightbox.classList.contains("active")) return;

  if (e.key === "Escape") {
    closeLightbox();
  } else if (e.key === "ArrowLeft") {
    changeImage(-1);
  } else if (e.key === "ArrowRight") {
    changeImage(1);
  }
});